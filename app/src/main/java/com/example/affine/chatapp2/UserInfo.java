package com.example.affine.chatapp2;

import java.io.Serializable;

public class UserInfo implements Serializable {
    String name;
    int id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
